import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const mdPath = path.join(__dirname, '..', 'public', 'content', 'prep-guide.md')
const outPath = path.join(__dirname, '..', 'src', 'data', 'starStories.generated.ts')

const md = fs.readFileSync(mdPath, 'utf8')
const start = md.indexOf('## Story 1:')
const end = md.indexOf('## LP Coverage Map')
if (start === -1 || end === -1) throw new Error('Could not locate stories block in prep-guide.md')

const block = md.slice(start, end)
const storyChunks = block.split(/(?=## Story \d+:)/g).filter((c) => c.startsWith('## Story'))

function strip(s) {
  return s
    .replace(/\*\*/g, '')
    .replace(/\r\n/g, '\n')
    .replace(/^\s+|\s+$/g, '')
}

function extractSection(chunk, headingLinePattern) {
  const m = chunk.match(headingLinePattern)
  if (!m || m.index === undefined) return ''
  const startIdx = m.index + m[0].length
  const tail = chunk.slice(startIdx)
  const next = tail.search(/\n### |\n## |\n\*\*🎯|\n---/)
  const body = next === -1 ? tail : tail.slice(0, next)
  return strip(body)
}

const labelToId = {
  'Deliver Results': 'deliver-results',
  Ownership: 'ownership',
  'Bias for Action': 'bias-for-action',
  'Dive Deep': 'dive-deep',
  'Earn Trust': 'earn-trust',
  'Think Big': 'think-big',
  'Invent and Simplify': 'invent-and-simplify',
  'Customer Obsession': 'customer-obsession',
  'Are Right, A Lot': 'are-right-a-lot',
  'Learn and Be Curious': 'learn-and-be-curious',
  'Hire and Develop the Best': 'hire-and-develop-the-best',
  'Insist on the Highest Standards': 'insist-on-the-highest-standards',
  Frugality: 'frugality',
  'Have Backbone': 'have-backbone',
}

function parseLps(chunk) {
  const line = chunk.split('\n').find((l) => l.includes('Primary LPs')) ?? ''
  const after = line.split(':').slice(1).join(':')
  const pieces = after.split(/,|⭐/).map((p) => p.trim()).filter(Boolean)
  const ordered = []
  for (const p of pieces) {
    for (const [label, id] of Object.entries(labelToId)) {
      if (p.includes(label) && !ordered.includes(id)) ordered.push(id)
    }
  }
  return ordered
}

function killer(chunk) {
  const m = chunk.match(/\*\*🎯 Why this is a killer story for AMXL JP:\*\*\s*([\s\S]*?)(?=\n---|\n## Story|$)/)
  return m ? strip(m[1]) : ''
}

const stories = storyChunks.map((chunk, idx) => {
  const titleLine = chunk.match(/## Story \d+: (.+)/)?.[1]?.trim() ?? `Story ${idx + 1}`
  const parts = titleLine.split(' - ')
  const company = parts[0]?.trim() ?? 'Unknown'
  const title = parts.slice(1).join(' - ').trim() || titleLine

  const situation = extractSection(chunk, /\n### SITUATION.*\n/)
  const task = extractSection(chunk, /\n### TASK.*\n/)
  const action = extractSection(chunk, /\n### ACTION.*\n/)
  const result = extractSection(chunk, /\n### RESULT.*\n/)
  const learning = extractSection(chunk, /\n### LEARNING\n/)

  const sections = [
    { label: 'situation', title: 'Situation (~30s)', content: situation, timingPct: 30 },
    { label: 'task', title: 'Task (~15s)', content: task, timingPct: 15 },
    { label: 'action', title: 'Action (~90s)', content: action, timingPct: 90 },
    { label: 'result', title: 'Result (~30s)', content: result, timingPct: 30 },
    { label: 'learning', title: 'Learning', content: learning, timingPct: 0 },
  ]

  return {
    id: `story-${idx + 1}`,
    title,
    company,
    primaryLpIds: parseLps(chunk),
    sections,
    killerAmxl: killer(chunk),
    estimatedMinutes: 2.75,
  }
})

const header = `/* eslint-disable */\n// AUTO-GENERATED by scripts/gen-star-stories.mjs — do not edit by hand.\nimport type { Story } from '@/types'\n\nexport const INITIAL_STORIES: Story[] = `

fs.writeFileSync(outPath, `${header}${JSON.stringify(stories, null, 2)} as Story[]\n`)

console.log('Wrote', outPath, 'stories:', stories.length)
